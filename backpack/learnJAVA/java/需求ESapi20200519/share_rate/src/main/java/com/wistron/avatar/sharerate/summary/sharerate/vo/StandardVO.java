package com.wistron.avatar.sharerate.summary.sharerate.vo;

public class StandardVO {
    /**
     * 價錢
     */
    String stprs;
    /**
     * 幣別
     */
    String waers;
    String werks;
    String matnr;
    String freidat;
    String cdate;
    String peinh;

    public String getStprs() {
        return stprs;
    }

    public void setStprs(String stprs) {
        this.stprs = stprs;
    }

    public String getWaers() {
        return waers;
    }

    public void setWaers(String waers) {
        this.waers = waers;
    }

    public String getWerks() {
        return werks;
    }

    public void setWerks(String werks) {
        this.werks = werks;
    }

    public String getMatnr() {
        return matnr;
    }

    public void setMatnr(String matnr) {
        this.matnr = matnr;
    }

    public String getFreidat() {
        return freidat;
    }

    public void setFreidat(String freidat) {
        this.freidat = freidat;
    }

    public String getCdate() {
        return cdate;
    }

    public void setCdate(String cdate) {
        this.cdate = cdate;
    }

    public String getPeinh() {
        return peinh;
    }

    public void setPeinh(String peinh) {
        this.peinh = peinh;
    }
}
